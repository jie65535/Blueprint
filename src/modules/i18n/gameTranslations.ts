// Combined lookup: tries all maps in order
const allMaps = [
    jokerNames, tarotNames, planetNames, spectralNames,
    voucherNames, blindNames, tagNames, deckNames, stakeNames,
    editionNames, sealNames, enhancementNames, packNames,
    miscSourceNames, suitNames, rankNames,
];

export function translateGameName(name: string): string {
    for (const map of allMaps) {
        if (name in map) return map[name];
    }
    return name;
}

// Translates "Rank of Suit" card names, e.g. "Ace of Hearts" → "红桃A"
export function translateCardName(name: string): string {
    const match = name.match(/^(.+) of (.+)$/);
    if (match) {
        const rankZh = rankNames[match[1]] ?? match[1];
        const suitZh = suitNames[match[2]] ?? match[2];
        return `${suitZh}${rankZh}`;
    }
    return translateGameName(name);
}

